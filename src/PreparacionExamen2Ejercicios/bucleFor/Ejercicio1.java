package PreparacionExamen2Ejercicios.bucleFor;

public class Ejercicio1 {
    public static void main(String[] args) {
        for (int i = 1; i<=10; i++){
            System.out.println("ascendente -> "+i);
        }
        for (int y = 10; y!=0; y--){
            System.out.println("desendente->"+y);
        }
    }
}
